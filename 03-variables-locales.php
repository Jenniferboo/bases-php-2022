    <h1>Variables locales</h1>

<p>En PHP, les variables sont représentées par un signe dollar "$" suivi du nom de la variable. Le nom est sensible à la casse.<br>
Les noms de variables suivent les mêmes règles de nommage que les autres entités PHP. Un nom de variable valide doit commencer par une lettre ou un souligné (_), suivi de lettres, chiffres ou soulignés. Exprimé sous la forme d'une expression régulière, cela donne : ^[a-zA-Z_\x80-\xff][a-zA-Z0-9_\x80-\xff]*$</p>
<p>on va éviter les caractère spéciaux, meme si ils sont valides. il faut absolument éviter les signes servant aux calcules</p>
    <h2>Utilité des variables</h2>

<p>Elles servent à stocker des information qui peuvent etre manipulées, affichées, sauvegardées, détruites etc..</p>
